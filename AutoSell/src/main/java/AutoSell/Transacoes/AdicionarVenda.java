    private void getClientes() {
        cbCliente.removeAllItems();
        LinkedList<Cliente> clientes = DadosAplicacao.INSTANCE.getClientes();
        for(Cliente cliente : clientes)
        {
            cbCliente.addItem(cliente.nif + " - " + cliente.getNome());
        }
    }

