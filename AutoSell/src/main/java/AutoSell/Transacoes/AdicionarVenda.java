    private void btnRegistarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistarActionPerformed
        String matricula = txtMatricula.getText();
        if(matricula.length()<=0)
        {
            throwMessageError("Insira uma matricula v√°lida");
        }
        else
        {
            if(txtValorVenda.getText().length()<=0)
            {
                throwMessageError("Tem que inserir um valor de venda");
            }
            else
            {
                int valorVenda = Integer.parseInt(txtValorVenda.getText());
                if(valorVenda<0)
                {
                    throwMessageError("O valor de Venda tem que ser superior a zero");
                }
                else
                {
                    Veiculo veiculo = DadosAplicacao.INSTANCE.getVeiculo(matricula);
                    if(veiculo==null)
                    {
                        throwMessageError("N√£o existe veiculo com essa matricula");
                    }
                    else
                    {
                        DadosAplicacao.INSTANCE.venderVeiculo(veiculo);
                        String dataAtual = obterDataAtual();
                        Transacao transacao = new Transacao(valorVenda, dataAtual, "venda");
                        boolean inseridoComSucesso = DadosAplicacao.INSTANCE.adicionarTransacao(transacao);
                        if(!inseridoComSucesso)
                        {
                            throwMessageError("Ocorreu um erro inesperado");
                        }
                        else
                        {
                            fechar();
                        }
                    }

                }
            }
        }
    }//GEN-LAST:event_btnRegistarActionPerformed

    private String obterDataAtual() {
        return new SimpleDateFormat("yyyy/MM/dd").format(Calendar.getInstance().getTime());
    }

