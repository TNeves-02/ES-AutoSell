    private void btnRegistarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistarActionPerformed
        String nome = txtNome.getText();
        String dataInicio = txtDataInicio.getText();
        String dataFim = txtDataFim.getText();
        boolean filial = jComboBoxFilial.getSelectedItem().toString().equals("Sim");
        String localizacao = txtLocalizacao.getText();
        if(nome.length()<=0 || dataInicio.length()<=0 || dataFim.length()<=0 || localizacao.length()<=0)
        {
            throwMessageError("Tem que preencher os campos do formulário");
        }
        else
        {
            Evento evento = new Evento(nome, dataInicio, dataFim, filial, localizacao, "Pendente");
            if(veiculosSeleccionados.size() <=0) {
                throwMessageError("Tem que selecionar veiculos");
            }
            else
            {
                for (Veiculo veiculo : veiculosSeleccionados) {
                    evento.addVeiculo(veiculo);
                }
                boolean resultado = DadosAplicacao.INSTANCE.adicionarEvento(evento);
                if(!resultado)
                {
                    throwMessageError("Ocorreu um erro inesperado");
                }
                else
                {
                    fechar();
                }
            }
        }
    }//GEN-LAST:event_btnRegistarActionPerformed

