    public void VisualizarVeiculos(List<Veiculo> veiculos) {
        DefaultTableModel modelotabela = new DefaultTableModel();

        tblVeiculos.setModel(modelotabela);
        modelotabela.addColumn("Matrícula");
        modelotabela.addColumn("Marca");
        modelotabela.addColumn("Modelo");
        modelotabela.addColumn("Adicionar");

        for (Veiculo veiculo : veiculos){
            modelotabela.addRow(new Object[]{
                    veiculo.getMatricula(),
                    veiculo.getMarca(),
                    veiculo.getModelo(),
                    "X"
            });
        }

        tblVeiculos.addMouseListener(new MouseAdapter() {
            public void mousePressed(MouseEvent mouseEvent) {
                JTable table =(JTable) mouseEvent.getSource();
                Point point = mouseEvent.getPoint();
                int row = table.rowAtPoint(point);
                if (mouseEvent.getClickCount() == 2 && table.getSelectedRow() != -1) {
                    if (table.getSelectedColumn() >=0 && table.getSelectedColumn() <=4){
                        if(!veiculosSeleccionados.contains(veiculos.get(row)))
                        {
                            veiculosSeleccionados.add(veiculos.get(row));
                        }
                    }
                }
            }
        });
    }

